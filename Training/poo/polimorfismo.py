# Quando funções (sendo pré-definidas da liguagem ou não)
# Realizam tarefas diferentes em determinadas situações
# De acordo com o que tal função recebe

# A função do sinal "+" variando de acordo com a situação
contatencao = "a" + "b"
soma = 1 + 2

print(contatencao)
print(soma)

nome = ("Gustavo")
lista = [1, 2, 3, 4, 5]

# Função print se adaptando ao dado recebido
print(nome[2])
print(lista[2])